	<h1>Soft fruits</h1>
	<p style="margin: 35px; font-size: 25px;">Soft Fruit is a general category of fruit, that mostly describes how it needs to be handled.The category includes many berries — such as blackberries, black currants, blueberries, gooseberries, grapes, loganberries, 
	raspberries, strawberries, etc.When sold fresh, Soft Fruit is usually sold in punnets (except for grapes) to both protect the fruit, and keep it together in the quantities being offered. </p>
	<p style="margin: 35px; font-size: 25px;">Soft Fruit has a short storage and shelf life after picking, so it is often sold frozen for cooking with.

As many of the fruits called Soft Fruit grow on bushes, growing Soft Fruit at home can allow fruit in a small garden that doesn’t have the room for fruit trees. Most Soft Fruit brushes need pruning once a year to get rid of old growth that might not bear fruit any longer.</p>
