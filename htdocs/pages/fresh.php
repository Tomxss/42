	<p style="margin: 35px; font-size: 25px;">The energy content of whole, fresh fruit derives primarily from simple sugars, which are currently under heightened scrutiny for their potential contribution to obesity and chronic disease risk. Yet fruit also has a 
	relatively low energy density, moderate palatability/reward value, and high fiber content, which together may limit energy intake. 
	Although reasoned arguments can be made that fruit is fattening or slimming, the question is best resolved empirically.</p>
	<p style="margin: 35px; font-size: 25px;">Current evidence suggests that whole, fresh fruit consumption is unlikely to contribute to excess energy intake and adiposity, but rather has little effect on these outcomes or constrains them modestly. Single-meal RCTs, RCTs lasting 3â€“24 weeks, and long-term observational studies are relatively consistent in supporting this conclusion. Whole, fresh fruit probably does not contribute to obesity and may have a place in the prevention and management of excess adiposity.</p>
