	<h1>Firm cheese</h1>
	<p style="margin: 35px; font-size: 25px;">Firm cheeses have a texture which feels firmer than the semi-soft category and contains even less moisture. In semi-firm cheeses, the curd had been cut with a smaller grain, which in turn releases more whey. The majority of semi-firm cheeses are salted in brine during production. As these cheeses age, they become firmer. This is the most important category with its large number of cheeses and the popularity of its varieties. cheeses—grating cheeses such as Grana Padano, Parmesan or Pecorino—are quite firmly packed into large forms and aged for months or years. Firm cheeses deserve a place on any great cheese plate.</p>
	<p style="margin: 35px; font-size: 25px;">They tend to have nutty and complex savory notes.<br>Firm cheeses are unequalled when it comes to adding sharpness to vegetables, omelettes, au gratin dishes, quiches, pizzas and soups.</p>
