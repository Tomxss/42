	<h1>Blue cheese</h1>
	<p style="margin: 35px; font-size: 25px;">Blue-veined cheeses are more commonly called “blue” because of the bluish or greenish veins that furrow the interior. Production is similar to that of soft or non-cooked semi-soft, with one important exception: you incorporate a culture (penicillium glaucum roqueforti or penicillium candidum) with the curdled milk to promote the development of mould in the interior. The history of blue cheese goes back to the 7th century, to a cave outside the village of Roquefort in France. Legend has it that a distracted shepherd forgot his lunch of bread and cheese in the cave. When he returned a few months later, the cheese had become infested with penicillium roqueforti, a mold that was growing in the cave. </p>
	<p style="margin: 35px; font-size: 25px;">This type of cheese is eaten plain with fresh fruit or nuts. It is also served with cooked meats, pastas or vegetables. It can also thicken a sauce, enhance a fondue or give dip a tangy flavour.</p>
