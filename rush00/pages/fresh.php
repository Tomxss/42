	<h1>Fresh cheese</h1>
	<p style="margin: 35px; font-size: 25px;">Fresh cheeses are obtained simply by leaving milk out in the ambient air to allow it to curdle naturally. The curdled milk is then poured into a small basket with holes (the “cheese drainer”), which allows the whey to drain out and gives the cheese its final form. The addition of ferments acidifies the milk, transforming it into firm curdles that are crumbly, permeable and delicate. Fresh cheeses have a moisture content higher than 60%, and they have a more or less dense texture that can be liquid, smooth or creamy. These cheeses need to be eaten quickly after they are made. Ricotta cheese is also part of this category.</p>
	<p style="margin: 35px; font-size: 25px;">Fresh cheese such as the Ricotta from the Abbaye de Saint-Benoît-du-Lac can be served with crackers, and in sandwiches and salads. It is also perfect for making delicious dessert recipes or for stuffing meat and vegetables.</p>
